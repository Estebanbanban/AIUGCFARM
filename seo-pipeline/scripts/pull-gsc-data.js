/**
 * pull-gsc-data.js
 * Pulls Search Console data via service account and writes to gsc-data.json.
 * Fetches: per-page performance + per-query-per-page breakdown (last 90 days).
 */

import { google } from 'googleapis';
import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join, resolve } from 'path';
import { config } from 'dotenv';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PIPELINE_DIR = resolve(__dirname, '..');

config({ path: join(PIPELINE_DIR, '.env') });

const CREDENTIALS_PATH = join(PIPELINE_DIR, 'gsc-credentials.json');
const OUTPUT_PATH = join(PIPELINE_DIR, 'gsc-data.json');
const SITE_URL = process.env.GSC_SITE_URL || 'sc-domain:cinerads.com';

function dateStr(daysAgo = 0) {
  const d = new Date();
  d.setDate(d.getDate() - daysAgo);
  return d.toISOString().split('T')[0];
}

async function pullGscData() {
  console.log('Pulling GSC data...');
  console.log(`  Site: ${SITE_URL}`);

  const credentials = JSON.parse(readFileSync(CREDENTIALS_PATH, 'utf-8'));

  const auth = new google.auth.GoogleAuth({
    credentials,
    scopes: ['https://www.googleapis.com/auth/webmasters.readonly'],
  });

  const webmasters = google.webmasters({ version: 'v3', auth });

  const startDate = dateStr(90);
  const endDate = dateStr(0);
  console.log(`  Date range: ${startDate} to ${endDate}`);

  let pageRows = [];
  let queryRows = [];

  try {
    // 1. Per-page performance
    const pageRes = await webmasters.searchanalytics.query({
      siteUrl: SITE_URL,
      requestBody: {
        startDate,
        endDate,
        dimensions: ['page'],
        rowLimit: 1000,
      },
    });

    pageRows = (pageRes.data.rows || []).map((row) => ({
      page: row.keys[0],
      clicks: row.clicks,
      impressions: row.impressions,
      ctr: Math.round(row.ctr * 10000) / 100,
      position: Math.round(row.position * 10) / 10,
    }));

    console.log(`  Pages found: ${pageRows.length}`);
  } catch (err) {
    console.warn(`  GSC pages query failed: ${err.message}`);
    console.warn('  Check that the service account has access to the GSC property.');
  }

  try {
    // 2. Per-query per-page (what queries each page ranks for)
    const queryRes = await webmasters.searchanalytics.query({
      siteUrl: SITE_URL,
      requestBody: {
        startDate,
        endDate,
        dimensions: ['page', 'query'],
        rowLimit: 5000,
      },
    });

    queryRows = (queryRes.data.rows || []).map((row) => ({
      page: row.keys[0],
      query: row.keys[1],
      clicks: row.clicks,
      impressions: row.impressions,
      ctr: Math.round(row.ctr * 10000) / 100,
      position: Math.round(row.position * 10) / 10,
    }));

    console.log(`  Query-page pairs found: ${queryRows.length}`);
  } catch (err) {
    console.warn(`  GSC queries query failed: ${err.message}`);
  }

  const output = {
    version: '2.0',
    note: 'Auto-generated by pull-gsc-data.js. Do not edit manually.',
    domain: 'cinerads.com',
    siteUrl: SITE_URL,
    lastUpdated: new Date().toISOString(),
    dateRange: { start: startDate, end: endDate },
    pages: pageRows,
    queries: queryRows,
  };

  writeFileSync(OUTPUT_PATH, JSON.stringify(output, null, 2));
  console.log(`Written to gsc-data.json`);

  return output;
}

pullGscData().catch((err) => {
  console.error('pull-gsc-data failed:', err.message);
  process.exit(1);
});
